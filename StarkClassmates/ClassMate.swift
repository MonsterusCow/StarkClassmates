import Foundation

class ClassMate {
    
    var Name: String
    var Nick: String
    var Age: Int
    var FavoriteColor: String
    var Grade: Double
    
    init(Name: String, Nick: String, Age: Int, FavoriteColor: String, Grade: Double) {
        self.Name = Name
        self.Nick = Nick
        self.Age = Age
        self.FavoriteColor = FavoriteColor
        self.Grade = Grade
    }
}
